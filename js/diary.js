let currentEntry = 0;

function loadEntry() {
  document.getElementById("memoryPhoto").src = entries[currentEntry].image;
  document.getElementById("entryDate").innerText = entries[currentEntry].date;
  document.getElementById("entryLocation").innerText = entries[currentEntry].location;
  document.getElementById("entryText").innerText = entries[currentEntry].text;
  document.getElementById("pageNumber").innerText =(currentEntry + 1) + " / " + entries.length;
}

function animateEntryChange(direction) {

  const entry = document.getElementById("entryText");
  const photo = document.getElementById("memoryPhoto");

  entry.classList.add("flip-out");
  photo.classList.add("blur-out");

  setTimeout(() => {

    if (direction === "next" && currentEntry < entries.length - 1) {
      currentEntry++;
    }

    if (direction === "prev" && currentEntry > 0) {
      currentEntry--;
    }

    loadEntry();

    entry.classList.remove("flip-out");
    entry.classList.add("flip-in");

    photo.classList.remove("blur-out");
    photo.classList.add("blur-in");

    setTimeout(() => {
      entry.classList.remove("flip-in");
      photo.classList.remove("blur-in");
    }, 350);

  }, 350);
}

function nextEntry() {
  if (currentEntry < entries.length - 1) {
    animateEntryChange("next");
  } else {
    closeDiary();
  }
}

function prevEntry() {
  animateEntryChange("prev");
}

function closeDiary() {
  const cover = document.getElementById("cover");
  const spread = document.getElementById("spread");

  spread.style.display = "none";
  cover.style.display = "flex";

  setTimeout(() => {
    cover.classList.remove("open");
  }, 50);
}

